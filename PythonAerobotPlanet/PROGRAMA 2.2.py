calif = int(input("Ingresa la calificacion: "))

if(calif == 100):
    print("Exelente")
elif(calif >= 90):
    print("Muy bien")
elif(calif >= 80):
    print("Bien")
elif(calif >= 70):
    print("Regular")
elif(calif >= 60):
    print("Bajo")
elif(calif <= 59):
    print("Reprobado")
